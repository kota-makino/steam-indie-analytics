    def execute_sql_script(self, conn, sql_script: str, description: str):
        """SQL„Çπ„ÇØ„É™„Éó„ÉàÂÆüË°å"""
        cursor = conn.cursor()
        try:
            print(f"üõ†Ô∏è  {description} ÂÆüË°å‰∏≠...")
            
            # „Çπ„ÇØ„É™„Éó„Éà„ÇíÂÄãÂà•„ÅÆ„Çπ„ÉÜ„Éº„Éà„É°„É≥„Éà„Å´ÂàÜÂâ≤„Åó„Å¶ÂÆüË°å
            statements = [stmt.strip() for stmt in sql_script.split(';') if stmt.strip()]
            
            for i, statement in enumerate(statements, 1):
                if statement:
                    try:
                        cursor.execute(statement)
                        if "CREATE TABLE" in statement.upper():
                            table_name = statement.split("TABLE")[1].split("(")[0].strip()
                            print(f"   ‚úÖ „ÉÜ„Éº„Éñ„É´‰ΩúÊàê: {table_name}")
                        elif "CREATE INDEX" in statement.upper():
                            print(f"   üìä „Ç§„É≥„Éá„ÉÉ„ÇØ„Çπ‰ΩúÊàê: Statement {i}")
                        elif "CREATE VIEW" in statement.upper():
                            view_name = statement.split("VIEW")[1].split("AS")[0].strip()
                            print(f"   üëÅÔ∏è  „Éì„É•„Éº‰ΩúÊàê: {view_name}")
                    except Exception as e:
                        print(f"   ‚ö†Ô∏è  „Çπ„ÉÜ„Éº„Éà„É°„É≥„Éà {i} „Çπ„Ç≠„ÉÉ„Éó: {str(e)[:100]}...")
                        continue
            
            print(f"‚úÖ {description} ÂÆå‰∫Ü")
            
        except Exception as e:
            print(f"‚ùå {description} „Ç®„É©„Éº: {e}")
            raise
        finally:
            cursor.close()

